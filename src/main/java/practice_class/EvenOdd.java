package practice_class;

public class EvenOdd {

	public static void main(String[] args) {
		// find out even number or odd 
		
		int a = 100;
		int b = 9;
		
		if(a%2==0) {
			
			System.out.println("a is even number is:"+a);
		}else {
			System.out.println("a is odd number is"+a);
		}
		
		if(b%2==0) {
			System.out.println("b is even number is"+b);
		}else {
			System.out.println("b is odd number is"+b);
		}

	}

}
